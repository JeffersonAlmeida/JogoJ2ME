    public void inicia() {
        rodando = true;
        Thread t = new Thread(this);
        t.start();
    }

    public void pausa() {
        rodando = false;
    }

    private void setaCenario(int c) {
        switch (c) {
            case 1:
                cenario = cenario1;
                cenario_colisao = cenario_colisao1;
                break;
            case 2:
                cenario = cenario2;
                cenario_colisao = cenario_colisao2;
                break;
            case 3:
                cenario = cenario3;
                cenario_colisao = cenario_colisao3;
                break;
            default:
                cenario = cenario1;
                cenario_colisao = cenario_colisao1;
                break;
        }
    }

    public void movimenta() {
        int teclas = getKeyStates();
        if ((teclas & LEFT_PRESSED) != 0) {
            sprite.setDirecao(Personagem.E);
            sprite.mover(passos);
        } else if ((teclas & RIGHT_PRESSED) != 0) {
            sprite.setDirecao(Personagem.D);
            sprite.mover(passos);
        } else if ((teclas & UP_PRESSED) != 0) {
            sprite.setDirecao(Personagem.C);
            sprite.mover(passos);
        } else if ((teclas & DOWN_PRESSED) != 0) {
            sprite.setDirecao(Personagem.B);
            sprite.mover(passos);
        }
    }

    private void centralizaTela() {
        xOrig = sprite.getX() + sprite.getWidth() / 2 - largura_tela / 2;
        yOrig = sprite.getY() + sprite.getHeight() / 2 - altura_tela / 2;
        if (xOrig < 0) {
            xOrig = 0;
        } else if (xOrig > cenario.getWidth() - largura_tela) {
            xOrig = cenario.getWidth() - largura_tela;
        }

        if (yOrig < 0) {
            yOrig = 0;
        } else if (yOrig > cenario.getHeight() - altura_tela + 20) {
            yOrig = cenario.getHeight() - altura_tela + 20;
        }
    }

    private void trataCronometro(Calendar c) {
        if (cronometro.getEstado() == Cronometro.EXECUTANDO) {
            c = Calendar.getInstance();
            c.setTime(new Date());
            segundo_novo = c.get(Calendar.SECOND);
            if (segundo != segundo_novo) {
                cronometro.decrementa();
                segundo = segundo_novo;
            }
        }
    }

    private void verificaGanhou() {
        int x, y;
        x = sprite.getX();
        y = sprite.getY();
        if ((x >= 736) && (x <= 768) && (y >= 752) && (y <= 768)) {
            sprite.setEstado(Personagem.GANHOU);
        }
    }

    public String[] getRankings() {
        return rankings;
    }

    private void verificaColisao() {
        for (int n_passos = passos; n_passos > 0; n_passos--) {
            if (sprite.collidesWith(cenario_colisao, true)) {
                sprite.mover(-n_passos);
                if (n_passos > 1) {
                    sprite.mover(n_passos - 1);
                }
            } else {
                break;
            }
        }
    }

    public void run() {
        String data;
        boolean gravou = false;
        Calendar c = Calendar.getInstance();
        c.setTime(new Date());
        segundo = c.get(Calendar.SECOND);
        cronometro = new Cronometro(3);
        cronometro.executa();
        sprite.setPosition(largura_tela / 2, 70);
        while (rodando) {
            movimenta();
            verificaColisao();
            Graphics g = getGraphics();
            g.setColor(255, 255, 255);
            g.fillRect(0, 0, largura_tela, altura_tela);
            centralizaTela();
            gerente.setViewWindow(xOrig, yOrig, 200, 200);
            gerente.paint(g, 0, 0);
            trataCronometro(c);
            if ((cronometro.getEstado() == Cronometro.PARADO) && (sprite.getEstado() == Personagem.JOGANDO)) {
                sprite.setEstado(Personagem.PERDEU);
            }
            if ((sprite.getEstado() == Personagem.PERDEU) || (sprite.getEstado() == Personagem.GANHOU)) {
                sprite.trataEstado();
            } else {
                verificaGanhou();
                if ((sprite.getEstado() == Personagem.GANHOU) && !gravou) {
                    cronometro.para();
                    data = cronometro.getTempoDecorrido();
                    ranking.addRanking(data);
                    rankings = ranking.getRankings();
                    for (int i = 0; i < rankings.length; i++) {
                        System.out.println(rankings[i]);
                    }
                    gravou = true;
                }
            }
            if (sprite.getEstado() == Personagem.GANHOU) {
                g.drawString("WINNER!", 50, 30, Graphics.HCENTER & Graphics.VCENTER);
            } else if (sprite.getEstado() == Personagem.PERDEU) {
                g.drawString("TIME OUT!", 50, 30, Graphics.HCENTER & Graphics.VCENTER);
            }
            data = cronometro.getTempoRestante();
            g.drawString(data, 8, 8, Graphics.LEFT | Graphics.TOP);
            flushGraphics();
            try {
                Thread.sleep(30);
            } catch (InterruptedException e) {
            }
        }
    }

    public RankingDAO getRankingDAO() {
        return ranking;
    }

